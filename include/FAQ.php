    <h3>¿Cómo puedo presentar una sugerencia o una queja?</h3>

    <p>A través de la tramitación desde la Sede, del procedimiento de Quejas y Sugerencias.
    <h2>VALIDACIÓN DE DOCUMENTOS</h2>
    <h3>¿En qué consiste la validación de documentos?</h3>

    <p>A través de la tramitación desde la Sede, del procedimiento de Quejas y Sugerencias.

    <p>Organismo son auténticos, plenamente válidos y no han sufrido alteraciones.

    <p>Todos los documentos firmados electrónicamente en la aplicación incorporan un código de verificación generado automáticamente.

    <p>De esta forma, cuando se imprime una copia en papel, el código de verificación actúa como «localizador» del documento dentro del archivo electrónico del Organismo, dotando al documento en papel de la consideración de copia auténtica a todos los efectos legales.

    <p>Si un ciudadano u otra Administración reciben la copia en papel, pueden verificar su autenticidad de documento de forma muy sencilla.

    <p>Basta con que accedan a la Sede Electrónica del Organismo en la sección «validar documentos» y tecleen el código de identificación. En ese mismo momento podrán descargar la versión original del documento en formato electrónico y comprobar su autenticidad.
    <h2 name="tablon">TABLÓN DE ANUNCIOS</h2>
    <h3>¿Qué funcionalidad tiene el tablón de anuncios electrónico?</h3>

    <p>El tablón de anuncios electrónico permite sustituir o complementar la publicación en el tablón de anuncios tradicional, con todas las garantías, ya que es posible firmar y sellar electrónicamente los documentos para garantizar la fecha de publicación y su inalterabilidad a lo largo del tiempo.

    <p>De esta manera se favorece el acceso a la información, ya que el ciudadano podrá consultar el tablón de anuncios electrónico desde internet, evitando desplazamientos y facilitando la localización de documentos de un modo mucho más sencillo y cómodo.
    <h2 name="perfil">PERFIL DE CONTRATANTE</h2>
    <h3>¿Qué funcionalidad tiene el perfil de contratante?</h2>

    <p>Es un servicio que acredita, registra, deposita y pone a disposición del ciudadano mediante Internet toda la información relativa a la contratación administrativa realizada por el Organismo, tal como anuncios de licitación, las adjudicaciones, los pliegos de contratación…
    <h2 name="notifica">NOTIFICACIÓN ELECTRÓNICA</h2>
    <h3>¿Qué es una notificación electrónica?</h3>

    <p>Es el sistema que permite el envío de comunicaciones escritas a los interesados por un medio electrónico.

    <p>La notificación electrónica será válida jurídicamente a todos los efectos legales y como tal, a partir del acceso a su contenido, se iniciará el cómputo de los plazos que legalmente se establezcan.
    <h3>¿Cuándo se entenderá realizada la notificación electrónica?</h3>

    <p>La notificación se entenderá practicada cuando se acceda a su contenido.

    <p>El interesado dispondrá de un plazo de 10 días naturales desde la puesta a disposición de la notificación electrónica para acceder a su contenido.
    <h3>¿Cuándo se entiende rechazada la notificación electrónica?</h3>

    <p>Si transcurren 10 días naturales desde la puesta a disposición de la notificación sin que el interesado haya accedido a su contenido, esta se entenderá rechazada.

    <p>El rechazo de la notificación tendrá los efectos estipulados en el artículo 41.5 de la Ley 39/2015, de 1 de octubre del Procedimiento Administrativo Común de las Administraciones Públicas se y entenderá efectuado el trámite de notificación, siguiéndose el procedimiento.
    <h3>¿Qué es una notificación electrónica por comparecencia?</h3>

    <p>Una vez emitida la notificación, los interesados reciben de forma automática un mensaje en su correo electrónico personal requiriendo su comparecencia en la Sede. El acceso al buzón electrónico es totalmente seguro al realizarse mediante identificación con cualquiera de los certificados digitales admitidos.

    <p>La consulta de las notificaciones electrónicas implica la descarga de un acuse de recibo que certifica que la notificación se ha practicado.
