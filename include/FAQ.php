    <h2 name="sede"><a name="sede">Sede electrónica</a></h2>

    <h3>¿Qué es la Sede electrónica?</h3> 
    <p>La Sede Electrónica de este Organismo, es un medio a través del cual los ciudadanos pueden ejercer su derecho de acceso a la información, a los servicios y a los trámites electrónicos de la Corporación. Desde esta plataforma, el ciudadano podrá efectuar trámites o realizar consultas sin tener que asistir presencialmente a las oficinas municipales.
    </p>
    <h3>¿Qué servicios ofrece la Sede Electrónica?</h3>
    <p>Por medio de la Sede Electrónica el ciudadano puede realizar algunos trámites como solicitar una licencia de obras, pagar un impuesto, consultar el estado de tramitación de sus expedientes o recibir notificaciones electrónicas.
    </p>

    <h3>¿Cómo puedo utilizar los servicios de la Sede?</h3>
    <p>Deberá disponer de un certificado electrónico para identificarse y acceder a la documentación personal disponible a través de la Sede.
    <h3>¿Quién puede realizar trámites electrónicos?</h3>

    <p>La Sede Electrónica está dirigida a todos los ciudadanos. El único requisito para realizar trámites electrónicos es que hay que poseer un certificado digital admitido en la Sede.
    <h2 name="registro"><a name="registro">REGISTRO ELECTRÓNICO</a></h2>

    <h3>¿Qué es el registro electrónico?</h3>

    <p>El registro electrónico es análogo al registro presencial. Los ciudadanos podrán presentar documentación al Organismo a través de este registro sin tener que perder tiempo en desplazamientos y esperas, con el único requisito de identificarse debidamente de modo seguro mediante cualquiera de los certificados digitales admitidos.

    <p>Mediante el Registro electrónico cualquier procedimiento incluido en el catálogo de trámites de la Sede Electrónica puede iniciarse a través de Internet.
    <h3>¿Cómo funciona el registro electrónico?</h3>

    <p>El ciudadano podrá rellenar la instancia de solicitud desde la propia Sede y adjuntar todos los documentos pertinentes de un modo intuitivo gracias a un asistente que le guía paso a paso. El Registro electrónico admite además la presentación de cualquier otro tipo de escrito y comunicación, como quejas y sugerencias, y permite al ciudadano elegir si desea ser notificado electrónicamente o por medios tradicionales.

    <p>Una vez se ha presentado la instancia o documento, la aplicación genera de forma automática la anotación de entrada en el Registro Electrónico del Organismo. A su vez, desde la Sede el ciudadano puede imprimir o guardar en su equipo un acuse de recibo que permite acreditar legalmente el contenido presentado: datos de la anotación (número asignado, fecha y hora), datos relativos a la comprobación de la identidad del solicitante y una relación de todos los documentos aportados.
    <h3>¿Los trámites electrónicos deben realizarse en algún horario establecido?</h3>

    <p>No. Los trámites electrónicos no tienen ningún horario preestablecido. Se podrán realizar los trámites electrónicos en cualquier momento, 24 horas, los 365 días del año.
    <h3>¿Se puede realizar un trámite en un día inhábil?</h3>

    <p>Sí, se puede realizar un trámite electrónico en un día inhábil, pero a efectos legales, este se entenderá realizado en la primera hora del primer día hábil siguiente.
    <h3>¿Qué se considera día inhábil?</h3>

    <p>Son los días marcados como festivos en el calendario oficial de la Sede.
    <h2 name="tramite"><a name="tramite">TRÁMITES ELECTRÓNICOS</a></h2>
    <h3>¿Los trámites que se realizan desde la Sede tienen la misma validez que los realizados de forma presencial?</h3>

    <p>Sí, los trámites electrónicos tienen la misma validez que los realizados de forma presencial.
    <h3>¿Cómo puedo iniciar un trámite electrónico?</h3>

    <p>Desde el catálogo de trámites, una vez que hemos seleccionado el procedimiento que queremos iniciar, hay que acceder a tramitación electrónica y seguir los pasos del asistente de acceso con certificado digital.

    <p>Una vez que hayamos confirmado los datos del interesado, de la solicitud y en su caso se haya adjuntado la documentación necesaria, se generará un acuse de recibo que permitirá acreditar legalmente el contenido presentado y los datos de la anotación del Registro (fecha y hora oficial de la presentación).
    <h3>¿Qué ocurre si no sé qué modelo de instancia debo utilizar?</h3>

    <p>Cada procedimiento del catálogo de trámites tiene su instancia específica, si no sabe qué instancia corresponde al trámite que quiere iniciar, utilice el modelo de instancia general.
    <h3>¿Cómo puedo saber el estado de mi expediente?</h3>

    <p>A través de la carpeta electrónica, en la sección «consultas de expedientes» el ciudadano podrá consultar el estado de los expedientes con los que se haya relacionado en calidad de interesado o de tercero. Desde aquí podrá asimismo aportar nuevos documentos, cambiar sus datos de identificación y descargar los documentos emitidos y aportados al expediente.
    <h2 name="carpeta"><a name="carpeta"> Carpeta electrónica</a></h2>
    <h3>¿Qué es la Carpeta Electrónica?</h3>

    <p>Es un espacio donde el interesado puede relacionarse con el Organismo permitiendo agilizar las gestiones con la Administración.
    <h3>¿Cómo se puede acceder a la Carpeta Electrónica?</h3>

    <p>Los interesados pueden acceder a través de los Certificados Digitales admitidos en la Sede.
