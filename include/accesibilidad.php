    <h1>Contacto</h1>
    <div class="blanco">
        <p>Los sitios web de las administraciones públicas tienen el deber de cumplir los criterios de accesibilidad indicados en la norma UNE 139803:2012 . Esta normativa fija una serie de características que todo portal Web debe cumplir si quiere ser accesible, sirviendo además como base para la Certificación de Accesibilidad TIC (Accesibilidad Web) actualmente vigente en España.
        </p>
        <p>Así mismo, los sitios web de la administración deben satisfacer todos los puntos de verificación de prioridad 2 (adecuación de nivel Doble A) definidos en la especificación de Pautas de Accesibilidad al Contenido en la Web (WCAG 2.0) por la Iniciativa de Accesibilidad Web (WAI) del Consorcio World Wide Web (W3C).
        </p>
        <p>Este sitio se ha diseñado adaptándose a los estándares y normativas vigentes en relación a la accesibilidad, para lo cual se realizan revisiones previas a cada publicación de nuevos contenidos. En cualquier caso, es posible que encuentre alguna página que no se adapte al 100% a estos estándares, ya que algunas de las secciones contenidas en el portal están en proceso de adaptación y mejora. Si usted experiementa alguna dificultad en el acceso la información contenida en este sitio web, por favor, contacte con nosotros. Realizaremos las mejoras necesarias a la mayor brevedad posible.
        </p>
        <h3>Teclas de acceso rápido</h3>
        <p>Pulsando una combinación de teclas podrá acceder rápidamente a determinadas páginas o contenidos de la página.
        </p>
        <ul>
            <li>
                <span>Tecla 1:</span>  Página de incio
            </li>
            <li>
                <span>Tecla 2:</span>  Catálogo de trámites
            </li>
            <li>
                <span>Tecla 3:</span>  Carpeta electrónica
            </li>
        </ul>


        <p>Dependiendo del navegador que utilice, el procedimiento es distinto. A continuación explicamos el funcionamiento para los navegadores más habituales.
        </p>
        <ul>
            <li><span>Internet Explorer</span>: Alt + tecla + Intro</li>
            <li><span>Mozilla, Firefox y Chrome</span>: Alt + tecla (en Firefox 2: Alt + Mayúsculas + tecla)</li>
            <li><span>Opera</span>: Mayúsculas + Esc + tecla</li>
            <li><span>Safari</span>: Cmd + tecla</li>
        </ul>

        <h3>Tamaño del texto</h3>
        <p>Los tamaños de las fuentes están definidos con unidades relativas para que pueda ampliar o disminuir el tamaño de la fuente desde las opciones del navegador:
        </p>
        <ul>
            <li><span>Internet Explorer, Mozilla y Firefox</span>: Ver > Tamaño del texto</li>
            <li><span>Opera</span>: Ver > Zoom</li>
            <li><span>Safari</span>: Ver > Hacer el texto más grande</li>
            <li><span>Chrome</span>: Controla la página actual > Tamaño del texto</li>
        </ul>

        <h3>Para modificar el tamaño de todo en la página (efecto zoom):</h3>

        <ul>
            <li><span>Ctrl</span> + <span>+</span> para aumentarlo</li>
            <li><span>Ctrl</span> + <span>-</span> para disminuirlo</li>
            <li><span>Ctrl</span> + <span>0</span> restaura el tamaño original</li>
        </ul>

        <h3>Cambiar a alto contraste</h3>
        <p>En la parte superior derecha de la cabecera se dispone de una opción de alto contraste que muestra el fondo en color negro y para el texto en blanco, de esta forma las personas con problemas de visión pueden acceder a la información más cómodamente. Se puede volver a la presentación original pulsando de nuevo.
        </p>

